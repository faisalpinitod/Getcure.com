function sortfunc(){
    let s=document.querySelector("#shift1").value
    
    if(s=="low"){
        let sortdata=data.sort((a,b)=>{
            return (a.price) - (b.price)
        })
        displaymain2(sortdata)
    }else{
        let sortdata=data.sort((a,b)=>{
            return (b.price) - (a.price)
        })
        displaymain2(sortdata)
    }
}
